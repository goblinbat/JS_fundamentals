var friend = "Bob";

switch (friend) {
    case "Autumn":
        console.log("Hey Autumn, are you going rock climbing?");
        break;
    case "Dave":
        console.log("Hey Dave, how is Cooper?");
        break;
    case "Alecx":
        console.log("Hey Alecx, when are we playing DnD?");
        break;
    default:
        console.log(`I'm sorry, ${friend}, but do I know you?`)
}


