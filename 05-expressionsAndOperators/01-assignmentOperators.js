let r = 5
// The 3 following lines are all the same
r = r + 1
r += 1
r ++
// As are these 3:
r = r - 1
r -= 1
r --
// And these 2:
r = r * 1
r *= 1
// And these 2:
r = r / 1
r /= 1
// and these!
r = r % 1
r %= 1

// Remainder/ Modulus
r %= 2 // 5/2 => 2 remainder 1 => returns 1 

// Exponents
r **= 1   // use double asterisks (**) to square: 2** = 4
